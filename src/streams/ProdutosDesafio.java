package streams;

public class ProdutosDesafio {

	final String nome;
	final Double preco;
	final Double lucro;
	final Integer estoque;
	
	
	public ProdutosDesafio(String nome, Double preco, Double lucro, Integer estoque) {
		super();
		this.nome = nome;
		this.preco = preco;
		this.lucro = lucro;
		this.estoque = estoque;
	}
}
